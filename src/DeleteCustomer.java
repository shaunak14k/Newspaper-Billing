    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        
        String contactNo = contactNumberTextField.getText();
        
        try
        {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from Customer where cust_contact_no ='"+contactNo+"'");
            
            if(rs.next())
            {
                nameTextField.setText(rs.getString(1));
                contactNumberFormTextField.setText(rs.getString(2));
                emailTextField.setText(rs.getString(3));
                addressTextField.setText(rs.getString(4));
                genderComboBox.setSelectedItem(rs.getString(5));
            
                contactNumberTextField.setEditable(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Contact Number does not exist");
            }
                    
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

