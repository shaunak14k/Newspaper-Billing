    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:

        int confirm = JOptionPane.showConfirmDialog(null, "Do you really want to Delete this Customer","Delete Customer Records", JOptionPane.YES_NO_OPTION);
        
        if(confirm==0)
        {
            
            int confirm1 = JOptionPane.showConfirmDialog(null, "WARNING! - All the Subscriptions related with this Customer will also be Deleted. Do you want to Delete this Customer Record ?","WARNING", JOptionPane.YES_NO_OPTION);
            if(confirm1==0)
            {
             
                String custContactNoSearch = contactNumberTextField.getText();

                try
                {
                    Connection con = ConnectionProvider.getCon();
                    Statement st = con.createStatement();

                    st.executeUpdate("delete from Customer where cust_contact_no='"+custContactNoSearch+"' ");

                    JOptionPane.showMessageDialog(null, "Successfully Deleted");

                    setVisible(false);
                    new DeleteCustomer().setVisible(true);
                }

                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
            }

        }
        
        
    }//GEN-LAST:event_updateButtonActionPerformed

